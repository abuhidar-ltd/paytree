export function SocialHubModule({ config, span = 2, className = "" }: SocialHubModuleProps) {
  const spanClass = span === 2 ? "col-span-2" : ""
  const links = config?.links || []
  const displayLinks = links.slice(0, 4) // Max 4 icons
  
  return (
    <motion.div
      className={`glass-brick ${spanClass} ${className}`}
      whileHover={{ scale: 1.02 }}
    >
      <div className="p-4">
        <div className="label mb-3">Connect</div>
        
        <div className={`
          grid gap-3
          ${config.layout === "row" ? "grid-cols-4" : "grid-cols-4"}
        `}>
          {displayLinks.map((link, index) => {
            const platformConfig = PLATFORM_CONFIG[link.platform.toLowerCase()] || {
              icon: (
                <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M13.828 10.172a4 4 0 00-5.656 0l-4 4a4 4 0 105.656 5.656l1.102-1.101m-.758-4.899a4 4 0 005.656 0l4-4a4 4 0 00-5.656-5.656l-1.1 1.1" />
                </svg>
              ),
              gradient: "from-[#00ff88] to-[#00cc6a]",
              label: link.platform,
            }
            
            return (
              <motion.a
                key={index}
                href={link.url}
                target="_blank"
                rel="noopener noreferrer"
                className={`
                  aspect-square rounded-2xl
                  bg-gradient-to-br ${platformConfig.gradient}
                  flex items-center justify-center
                  text-white
                  shadow-lg hover:shadow-xl
                  transition-shadow duration-300
                `}
                whileHover={{ scale: 1.1, y: -2 }}
                whileTap={{ scale: 0.95 }}
                title={platformConfig.label}
              >
                {platformConfig.icon}
              </motion.a>
            )
          })}
          
          {/* Empty slots */}
          {Array.from({ length: Math.max(0, 4 - displayLinks.length) }).map((_, i) => (
            <div
              key={`empty-${i}`}
              className="aspect-square rounded-2xl bg-[rgba(255,255,255,0.02)] border border-dashed border-[rgba(255,255,255,0.1)]"
            />
          ))}
        </div>
      </div>
    </motion.div>
  )
}
